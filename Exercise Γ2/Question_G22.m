X=[ 21.0000,-3.0000 + 5.1962i,-3.0000 + 1.7321i,-3.0000,-3.0000 - 1.7321i, -3.0000 - 5.1962i];
%Αποθηκεύουμε το μήκος του διανύσματος και δημιουργούμε δύο κατάλληλους
%πίνακες y και z
N=length(X);
y=0:1:N-1;
z=0:1:N-1;
%πολλαπλασιασμό πινάκων (αντίστροφου πίνακα y με τον πίνακα z)
yz=y'*z;
WN=exp(-1i*2*pi/N);
WNyz=WN.^(-yz);
xy=(X*WNyz)/N;
